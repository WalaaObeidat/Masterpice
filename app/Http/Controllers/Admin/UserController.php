     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $Users = User::findorFail($id);

        if ($Users->is_admin == 0){
            
            $Users->is_admin = 1;
        }
        
        else{
            $Users->is_admin = 0;
        }
        
        $Users->save();
        return redirect()->route('users.index');
    }

